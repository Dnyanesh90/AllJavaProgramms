package LinkedList;

public class Demo3 {

	public static void main(String[] args) {
		int a[]={1,2,5,6,3,2,4,8,9,10,12,11};  
		System.out.println("Odd Numbers:");
		for(int i=0;i<a.length;i++){  
		if(a[i]%2!=0){	
		System.out.println(a[i]);  
		}  
		} 
		
		System.out.println("Even Numbers:");
		
		for(int i=0;i<a.length;i++){  
		if(a[i]%2==0){  
		System.out.println(a[i]);  

	}

}
	}
}
