package Tutorial1;

public class Demo10 {

	public static void main(String[] args) {
		int a=90;
		int b=50;
		
		System.out.println(a==b);//false
		System.out.println(a!=b);//true

		System.out.println(a>b);//true
		System.out.println(a>=b);//true
		
		System.out.println(a<b);//false
		System.out.println(a<=b);//false
	}

}
