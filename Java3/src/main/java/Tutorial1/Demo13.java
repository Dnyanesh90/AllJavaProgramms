package Tutorial1;

public class Demo13 {

	public static void main(String[] args) {
		int a=10;
		System.out.println(a);//10
		
		a+=5;		//a=a+5;
		System.out.println(a);//15
			
		a-=10;		//a=a-10;
		
		System.out.println(a);//5
		
		a*=5;			//a=a*5;
		System.out.println(a);//25
		
		

	}

}
